    public void uppdateraMetaBana(Spelare spelaren)
    {
        for(int indexGubbe = 0; indexGubbe < spelaren.getAntalGubbar(); indexGubbe++)
        {
            fyllMetaMatris(spelaren.getGubbe(indexGubbe).getXKord(), 
                    spelaren.getGubbe(indexGubbe).getYKord(), spelaren.getGubbe(indexGubbe));
        }
        for(int indexSkott = 0; indexSkott < spelaren.getAntalSkott(); indexSkott++)
        {
            fyllMetaMatris(spelaren.getSkott(indexSkott).getXKord(), 
                    spelaren.getSkott(indexSkott).getYKord(), spelaren.getSkott(indexSkott));
        }
        for(int indexByggnad = 0; indexByggnad < spelaren.getAntalByggnader(); indexByggnad++)
        {
            for(int xIndex = spelaren.getByggnad(indexByggnad).getXKord(); 
                    xIndex <= spelaren.getByggnad(indexByggnad).getXKord() + 
                    spelaren.getByggnad(indexByggnad).getStorlekX() - 1
                    ;xIndex++)
            {
                for(int yIndex = spelaren.getByggnad(indexByggnad).getYKord(); 
                    yIndex <= spelaren.getByggnad(indexByggnad).getYKord() + 
                        spelaren.getByggnad(indexByggnad).getStorlekY() - 1
                    ;yIndex++)
                {
                    fyllMetaMatris(xIndex, 
                    yIndex, spelaren.getByggnad(indexByggnad));
                }
            }
        }
    }
    
    public void uppdateraNaturBana(Natur natur)
    {
        for(int indexNatur = 0; indexNatur < natur.getAntalObjekt(); indexNatur++)
        {
            fyllNaturMatris(natur.getEnhet(indexNatur).getXKord(), 
                    natur.getEnhet(indexNatur).getYKord(), natur.getEnhet(indexNatur));
        }
    }
    
