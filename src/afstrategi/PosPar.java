package afstrategi;


public class PosPar
{
    private int x;
    private int y;
    
    public PosPar(int x, int y)
    {
        this.x = x;
        this.y = y;
    }   
    
    public int getX()
    {
        return x;
    }
    
    public int getY()
    {
        return y;
    }  
}
